    public function destroy(PostModel $admin_post)
    {
        // $post = PostModel::where('id',$post->id)->first();
        // dd($admin_post);
        $likes = Like::where('post_id',$admin_post->id)->get();
        foreach($likes as $like)
        {
            $like->delete();
        }
        $admin_post->delete();
        // dd($post);
        return redirect()->back()->with('status','Post is deleted successfully.');
    }
